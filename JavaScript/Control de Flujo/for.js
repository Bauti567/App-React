// El ciclo for permite iterar un cilo 
// Hasta que se cumpla una condicion

for(let i = 0; i <= 10; i++){
    console.log(i)
}

// Iterar dentro de un arreglo

const numeros = ['Arreglo',1,2,3,4,5]

// La funcion lenght me permite traer el largo de un arreglo

console.log('Este es el Arreglo');

for(let i = 0; i <= numeros.length; i++){
    console.log(numeros[i]);
}