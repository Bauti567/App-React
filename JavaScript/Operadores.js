function Suma(n1,n2){
    return n1 + n2;
}

function Resta(n1,n2){
    return n1 - n2;
}


function Multiplicacion(n1,n2){
    return n1 * n2;
}

function Division(n1,n2){
    return n1 / n2;
}


console.log(Suma(2 ,2),Resta(5,3),Multiplicacion(5,4),Division(5,2))

